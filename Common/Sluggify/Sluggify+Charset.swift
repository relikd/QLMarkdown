extension GithubSlugger {
	static func slugify(_ str: String) -> String {
		var rv = str.lowercased().unicodeScalars
		rv.removeAll(where: GH_SLUG_BAD_CHARS.contains)
		return String(String.UnicodeScalarView(rv.map { $0 == " " ? "-" : $0 }))
	}
}



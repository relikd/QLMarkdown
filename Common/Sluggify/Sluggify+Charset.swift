/// Easier to understand an to maintain, but slower in processing.
private extension String.UnicodeScalarView.Element {
	/// `str.unicodeScalars.filter(\.keepSlugChar)`
	var keepSlugChar: Bool {
		switch properties.generalCategory {
		case .uppercaseLetter: true
		case .lowercaseLetter: true
		case .titlecaseLetter: true
		case .modifierLetter: true
		case .otherLetter: true
		case .nonspacingMark: true
		case .spacingMark: true
		case .enclosingMark: true
		case .decimalNumber: true
		case .letterNumber: true
		case .otherNumber: false
		case .connectorPunctuation: true
		case .dashPunctuation: self == "-" // remove all except normal dash
		case .openPunctuation: false
		case .closePunctuation: false
		case .initialPunctuation: false
		case .finalPunctuation: false
		case .otherPunctuation: false
		case .mathSymbol: false
		case .currencySymbol: false
		case .modifierSymbol: false
		case .otherSymbol: properties.isAlphabetic // allow enclosed letters (box or circle)
		case .spaceSeparator: self == " " // remove all except normal space (should be: + \u{200c} + \u{200d})
		case .lineSeparator: false
		case .paragraphSeparator: false
		case .control: false
		case .format: false
		case .surrogate: false
		case .privateUse: false
		case .unassigned: false
		@unknown default: false
		}
	}
}

